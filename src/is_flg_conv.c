#include "ft_printf.h"

int	is_flg_conv(char c)
{
	if (is_flg(c) || c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' \
		|| c == 'u' || c == 'x' || c == 'X' || c == '%')
		return (1);
	else
		return (-1);
}
